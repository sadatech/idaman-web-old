    public function filter_news_report(Request $request)
    {
       $query = News::whereBetween('created_at', [$request->startDate, $request->endDate]);
       if ($request->has("status")) {
            $query->where('status', $request->status);
       }if ($request->has("kejadian")) {
            $query->where('category_news', $request->kejadian);
       }
       $data_report = Datatables::of($query)->make(true);
       return $data_report;
    }
